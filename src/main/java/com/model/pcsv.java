package com.model;

public class pcsv {
	private int MaHD;
	private String MaSV;
	public pcsv() {
		super();
	}
	public pcsv(int maHD, String maSV) {
		super();
		MaHD = maHD;
		MaSV = maSV;
	}
	public int getMaHD() {
		return MaHD;
	}
	public void setMaHD(int maHD) {
		MaHD = maHD;
	}
	public String getMaSV() {
		return MaSV;
	}
	public void setMaSV(String maSV) {
		MaSV = maSV;
	}
}
