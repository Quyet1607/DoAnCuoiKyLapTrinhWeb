package com.model;

public class pcgv {
	private int MaHD;
	private String MaGV;
	public pcgv() {
		super();
	}
	public pcgv(int maHD, String maGV) {
		super();
		MaHD = maHD;
		MaGV = maGV;
	}
	public int getMaHD() {
		return MaHD;
	}
	public void setMaHD(int maHD) {
		MaHD = maHD;
	}
	public String getMaGV() {
		return MaGV;
	}
	public void setMaGV(String maGV) {
		MaGV = maGV;
	}
}
